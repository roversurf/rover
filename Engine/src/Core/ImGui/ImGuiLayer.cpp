    ImGuiLayer::ImGuiLayer()
        : Layer("ImGuiLayer")
    {
    }

    void ImGuiLayer::OnAttach()
    {
        // Setup Dear ImGui context
        IMGUI_CHECKVERSION();
        ImGui::CreateContext();
        ImGuiIO& io = ImGui::GetIO(); (void)io;
        io.ConfigFlags |= ImGuiConfigFlags_NavEnableKeyboard;
        io.ConfigFlags |= ImGuiConfigFlags_DockingEnable;
        io.ConfigFlags |= ImGuiConfigFlags_ViewportsEnable;

        CQ_CORE_INFO("ImGui: Loading fonts...");

        // Try to load custom fonts, fallback to default if failed
        ImFont* boldFont = io.Fonts->AddFontFromFileTTF("Resources/Fonts/opensans/OpenSans-Bold.ttf", 18.0f);
        if (!boldFont)
        {
            CQ_CORE_WARN("Failed to load OpenSans-Bold.ttf from Resources/Fonts/opensans/");
        }
        
        ImFont* regularFont = io.Fonts->AddFontFromFileTTF("../../../Resources/Fonts/opensans/OpenSans-Regular.ttf", 18.0f);
        if (regularFont)
        {
            io.FontDefault = regularFont;
            CQ_CORE_INFO("ImGui: Custom fonts loaded successfully");
        }
        else
        {
            CQ_CORE_WARN("Failed to load OpenSans-Regular.ttf, using ImGui default font");
            CQ_CORE_WARN("Make sure Resources/Fonts/opensans/ folder exists next to the executable");
        }

        CQ_CORE_INFO("ImGui: Setting up style...");

        // Setup Dear ImGui style
        ImGui::StyleColorsDark();

        // When viewports are enabled we tweak WindowRounding/WindowBg so platform windows can look identical to regular ones.
        ImGuiStyle& style = ImGui::GetStyle();
        if (io.ConfigFlags & ImGuiConfigFlags_ViewportsEnable)
        {
            style.WindowRounding = 0.0f;
            style.Colors[ImGuiCol_WindowBg].w = 1.0f;
        }

        SetDarkThemeColors();

        CQ_CORE_INFO("ImGui: Initializing platform backends...");

        Application& app = Application::Get();
        GLFWwindow* window = static_cast<GLFWwindow*>(app.GetWindow().GetNativeWindow());

        // Setup Platform/Renderer backends
        [[maybe_unused]] bool glfwInitResult = ImGui_ImplGlfw_InitForOpenGL(window, true);
        CQ_CORE_ASSERT(glfwInitResult, "Failed to initialize ImGui GLFW backend!");
        
        [[maybe_unused]] bool openglInitResult = ImGui_ImplOpenGL3_Init("#version 410");
        CQ_CORE_ASSERT(openglInitResult, "Failed to initialize ImGui OpenGL3 backend!");

        CQ_CORE_INFO("ImGui: Initialization complete");
    }

    void ImGuiLayer::OnDetach()
    {
        ImNodes::DestroyContext();
        ImGui_ImplOpenGL3_Shutdown();
        ImGui_ImplGlfw_Shutdown();
        ImGui::DestroyContext();
    }

    void ImGuiLayer::OnEvent(Event& e)
    {
        if (m_BlockEvents)
        {
            ImGuiIO& io = ImGui::GetIO();
            e.Handled |= e.IsInCategory(EventCategoryMouse) & io.WantCaptureMouse;
            e.Handled |= e.IsInCategory(EventCategoryKeyboard) & io.WantCaptureKeyboard;
        }
    }

    void ImGuiLayer::Begin()
    {
        ImGui_ImplOpenGL3_NewFrame();
        ImGui_ImplGlfw_NewFrame();
        ImGui::NewFrame();
        ImGuizmo::BeginFrame();
    }

    void ImGuiLayer::End()
    {
        ImGuiIO& io = ImGui::GetIO();
        Application& app = Application::Get();
        io.DisplaySize = ImVec2((float)app.GetWindow().GetWidth(), (float)app.GetWindow().GetHeight());

        // Rendering
        ImGui::Render();
        ImGui_ImplOpenGL3_RenderDrawData(ImGui::GetDrawData());

        if (io.ConfigFlags & ImGuiConfigFlags_ViewportsEnable)
        {
            GLFWwindow* backup_current_context = glfwGetCurrentContext();
            ImGui::UpdatePlatformWindows();
            ImGui::RenderPlatformWindowsDefault();
            glfwMakeContextCurrent(backup_current_context);
        }
    }

